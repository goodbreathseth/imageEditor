public class Pixel {
    private int red;
    private int green;
    private int blue;

    public Pixel() {
        red = 0;
        green = 0;
        blue = 0;
    }

    public void setRed(int r) {
        red = r;
    }

    public void setGreen(int g) {
        green = g;
    }

    public void setBlue(int b) {
        blue = b;
    }

    public int getRed() {
        return red;
    }

    public int getBlue() {
        return blue;
    }

    public int getGreen() {
        return green;
    }
}
